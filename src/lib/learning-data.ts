export function getLearningModule(type: string): LearningModule | undefined {
  return LEARNING_MODULES[type]
}

export function getAllLearningModules(): LearningModule[] {
  return Object.values(LEARNING_MODULES)
}

export function getModulesByDifficulty(
  difficulty: "beginner" | "intermediate" | "advanced"
): LearningModule[] {
  return Object.values(LEARNING_MODULES).filter((module) => module.difficulty === difficulty)
}
